// kernel/src/color.rs

/// Common 32-bit color constants in BGR format (used by most framebuffers).
pub const BLACK: u32 = 0x00000000;
pub const WHITE: u32 = 0x00FFFFFF;
pub const RED:   u32 = 0x000000FF;
pub const GREEN: u32 = 0x0000FF00;
pub const BLUE:  u32 = 0x00FF0000;
pub const YELLOW: u32 = 0x0000FFFF;
pub const CYAN:   u32 = 0x00FFFF00;
pub const MAGENTA:u32 = 0x00FF00FF;
